/**
 * The AsciiArt class contains a main method that prints an ASCII elephant
 * to the console.
 */
public class AsciiArt {
    /**
     * Main method - prints ASCII art to the console.
     * 
     * @param args command line arguments
     */
    public static void main(String[] args) {
        System.out.println("            ___     ___");
        System.out.println("           /   \\~~~/   \\");
        System.out.println("   ,------(      ..     )");
        System.out.println("  /        \\___     ___/");
        System.out.println(" /|            (\\  |(");
        System.out.println("^  \\    /__\\    /\\ |");
        System.out.println("    |__|    |__|  -\"");

    } // main
} // AsciiArt